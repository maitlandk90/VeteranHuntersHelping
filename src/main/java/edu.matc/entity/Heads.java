package edu.matc.entity;

public class Heads {

    private int id;
    private String name;

    public Heads(){}

    public Heads(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
