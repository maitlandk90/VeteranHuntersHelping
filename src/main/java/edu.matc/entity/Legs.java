package edu.matc.entity;

public class Legs {

    private int id;
    private String name;

    public Legs(){}

    public Legs(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
